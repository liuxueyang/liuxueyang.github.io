class Solution {
public:
  int totalMoney(int n) {
    int res {};
    if (n <= 7) return (1 + n) * n / 2;

    int cur = 1, pre = (1 + 7) * 7 / 2;

    while (n >= 7) {
      n -= 7;
      res += pre;
      pre += 7;
      cur++;
    }

    while (n--) {
      res += cur++;
    }

    return res;
  }
};
