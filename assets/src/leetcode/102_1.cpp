/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode() : val(0), left(nullptr), right(nullptr) {}
 *     TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
 *     TreeNode(int x, TreeNode *left, TreeNode *right) : val(x), left(left), right(right) {}
 * };
 */
class Solution {
public:
  vector<vector<int>> levelOrder(TreeNode* ro) {
    vector<VI> res;

    if (!ro) return res;

    VI tmp;
    queue<pair<TNP, int>> q;
    int cur = 1;

    q.push({ro, 1});

    while (!q.empty()) {
      auto t = q.front();
      q.pop();

      auto ver = t.first;
      auto level = t.second;

      if (level != cur) {
        res.push_back(tmp);

        tmp = VI{ver->val};
        cur = level;
      } else {
        tmp.push_back(ver->val);
      }

      auto l = ver->left, r = ver->right;
      if (l) q.push({l, level + 1});
      if (r) q.push({r, level + 1});
    }

    if (!tmp.empty()) {
      res.push_back(tmp);
    }

    return res;
  }
};
