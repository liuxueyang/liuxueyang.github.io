class TextEditor {
public:
  string txt;
  int pos;

  TextEditor() {
    txt = "";
    pos = 0;
  }

  void addText(string text) {
    int sz = text.size();

    txt.replace(pos, 0, text);
    pos += sz;
  }

  int deleteText(int k) {
    if (!pos) return pos;

    if (pos >= k) {
      txt.replace(pos - k, k, "");
      pos -= k;
      return k;
    } else {
      k = pos;
      txt.replace(0, k, "");
      int ans = pos;
      pos = 0;
      return ans;
    }
    return -1;
  }

  string cursorLeft(int k) {
    pos -= k;
    pos = max(pos, 0);
    if (pos == 0) return "";

    int len = min(pos, 10);
    return txt.substr(pos - len, len);
  }

  string cursorRight(int k) {
    pos += k;
    pos = min(int(txt.size()), pos);
    int len = min(pos, 10);
    return txt.substr(pos - len, len);
  }
};

