class Solution {
public:
  vector<int> findKDistantIndices(vector<int>& nums, int key, int k) {
    set<int> s;
    VI res;
    int n = nums.size();

    for (int i = 0; i < n; ++i) {
      if (nums[i] == key) {
        for (int j = max(i - k, 0); j <= min(i + k, n - 1); ++j) {
          if (s.find(j) == s.end()) s.insert(j);
        }
      }
    }

    for (auto &x : s) res.push_back(x);

    return res;
  }
};
