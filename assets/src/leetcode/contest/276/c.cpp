class Solution {
public:
  long long mostPoints(vector<vector<int>>& q) {
    int n = q.size();
    vector<ll> d(n, 0);
    ll res = 0;

    d[n - 1] = q[n - 1][0];
    res = d[n - 1];

    for (int i = n - 2; i >= 0; --i) {
      int i1 = i + q[i][1] + 1;
      ll rest = 0;
      if (i1 <= n - 1) rest = d[i1];

      d[i] = max(q[i][0] + rest, d[i + 1]);
      res = max(res, d[i]);
    }

    return res;
  }
};
