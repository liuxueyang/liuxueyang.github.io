class Solution {
public:
  int totalMoney(int n) {
    int s0 = 7 + 7 * (7 - 1) / 2,
      n1 = n / 7, n2 = n % 7,
      a0 = n1 + 1;

    int res = n1 * s0 + n1 * (n1 - 1) / 2 * 7;
    res += n2 * a0 + n2 * (n2 - 1) / 2;

    return res;
  }
};
