/**
 * Definition for singly-linked list.
 * struct ListNode {
 *     int val;
 *     ListNode *next;
 *     ListNode() : val(0), next(nullptr) {}
 *     ListNode(int x) : val(x), next(nullptr) {}
 *     ListNode(int x, ListNode *next) : val(x), next(next) {}
 * };
 */
class Solution {
public:
  ListNode* swapPairs(ListNode* he) {
    if (!he) return he;

    LNP du = new LN(0, he);
    LN *p1, *p2, *p3, *pre, *res;

    pre = du;
    p1 = pre->next;
    p2 = p1->next;

    while (p1 && p2) {
      p3 = p2->next;
      pre->next = p2;
      p2->next = p1;
      p1->next = p3;
      pre = p1;
      p1 = p3;
      if (p1)
        p2 = p1->next;
    }

    res = du->next;
    delete du;

    return res;
  }
};
