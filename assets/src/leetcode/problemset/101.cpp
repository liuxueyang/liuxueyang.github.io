typedef ListNode LN;
typedef LN* LNP;
typedef TreeNode TN;
typedef TN* TNP;

/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode() : val(0), left(nullptr), right(nullptr) {}
 *     TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
 *     TreeNode(int x, TreeNode *left, TreeNode *right) : val(x), left(left), right(right) {}
 * };
 */
class Solution {
public:
  bool dfs(TNP ro1, TNP ro2) {
    if (!ro1 && !ro2) return true;
    else if (ro1 && ro2) {
      if (ro1->val != ro2->val) return false;
      return dfs(ro1->left, ro2->right) && dfs(ro1->right, ro2->left);
    } else if (ro1 || ro2) return false;

    return true;
  }

  bool isSymmetric(TreeNode* ro) {
    if (!ro) return true;
    auto l = ro->left, r = ro->right;
    return dfs(l, r);
  }
};
