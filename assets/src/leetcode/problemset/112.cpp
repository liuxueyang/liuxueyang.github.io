typedef ListNode LN;
typedef LN* LNP;
typedef TreeNode TN;
typedef TN* TNP;

/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode() : val(0), left(nullptr), right(nullptr) {}
 *     TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
 *     TreeNode(int x, TreeNode *left, TreeNode *right) : val(x), left(left), right(right) {}
 * };
 */
class Solution {
public:
  bool res;
  int ta;

  void dfs(TNP ro, int sum) {
    if (res) return;

    if (!ro) return;

    auto l = ro->left, r = ro->right;
    if (!l && !r) {
      if (sum == ta) {
        res = true;
      }
      return;
    }

    if (l) dfs(l, sum + l->val);
    if (r) dfs(r, sum + r->val);
  }

  bool hasPathSum(TreeNode* ro, int _ta) {
    res = false;
    ta = _ta;
    if (!ro) return false;

    dfs(ro, ro->val);

    return res;
  }
};
