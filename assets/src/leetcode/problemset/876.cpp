/**
 * Definition for singly-linked list.
 * struct ListNode {
 *     int val;
 *     ListNode *next;
 *     ListNode() : val(0), next(nullptr) {}
 *     ListNode(int x) : val(x), next(nullptr) {}
 *     ListNode(int x, ListNode *next) : val(x), next(next) {}
 * };
 */
class Solution {
public:
  ListNode* middleNode(ListNode* he) {
    LNP du = new LN(0, he);
    LNP p1 = du;
    LNP p2 = p1->next;

    while (p2 && p2->next) {
      p1 = p1->next;
      p2 = p2->next->next;
    }
    LNP tmp = p1->next;
    delete du;
    return tmp;
  }
};
