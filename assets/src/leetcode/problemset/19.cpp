/**
 * Definition for singly-linked list.
 * struct ListNode {
 *     int val;
 *     ListNode *next;
 *     ListNode() : val(0), next(nullptr) {}
 *     ListNode(int x) : val(x), next(nullptr) {}
 *     ListNode(int x, ListNode *next) : val(x), next(next) {}
 * };
 */
class Solution {
public:
  ListNode* removeNthFromEnd(ListNode* he, int n) {
    LNP du = new LN(0, he);
    LNP p1 {};
    LNP p2 {};
    LNP pre {};

    pre = du, p1 = pre->next; p2 = p1;
    while (p2 && n--) p2 = p2->next;
    while (p2) {
      pre = p1;
      p1 = p1->next;
      p2 = p2->next;
    }

    pre->next = p1->next;
    delete p1;
    LNP res = du->next;
    delete du;
    return res;
  }
};
