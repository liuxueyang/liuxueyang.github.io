/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode() : val(0), left(nullptr), right(nullptr) {}
 *     TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
 *     TreeNode(int x, TreeNode *left, TreeNode *right) : val(x), left(left), right(right) {}
 * };
 */
class Solution {
public:
  VI v1, v2;

  void dfs(TNP ro, VI &v) {
    if (!ro) return;

    auto l = ro->left, r = ro->right;
    if (!l && !r) {
      v.push_back(ro->val);
      return;
    }

    dfs(l, v);
    dfs(r, v);
  }

  bool leafSimilar(TreeNode* ro1, TreeNode* ro2) {
    v1 = VI{};
    v2 = VI{};

    dfs(ro1, v1);
    dfs(ro2, v2);

    return v1 == v2;
  }
};
