/**
 * Definition for singly-linked list.
 * struct ListNode {
 *     int val;
 *     ListNode *next;
 *     ListNode() : val(0), next(nullptr) {}
 *     ListNode(int x) : val(x), next(nullptr) {}
 *     ListNode(int x, ListNode *next) : val(x), next(next) {}
 * };
 */
class Solution {
public:
  ListNode* removeElements(ListNode* he, int val) {
    LNP du = new LN(0, he);

    LNP pre = du;
    LNP cur = pre->next;

    while (cur) {
      if (cur->val != val) {
        pre->next = cur;
        pre = cur;
      }
      cur = cur->next;
    }

    pre->next = cur;

    LNP res = du->next;
    delete du;
    return res;
  }
};
