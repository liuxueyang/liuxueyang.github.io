/**
 * Definition for singly-linked list.
 * struct ListNode {
 *     int val;
 *     ListNode *next;
 *     ListNode(int x) : val(x), next(NULL) {}
 * };
 */
class Solution {
public:
  ListNode *getIntersectionNode(ListNode *he1, ListNode *he2) {
    LNP p1 = he1;
    LNP p2 = he2;

    if (!p1 || !p2) return nullptr;

    while (p1 != p2) {
      if (p1) p1 = p1->next;
      else p1 = he2;

      if (p2) p2 = p2->next;
      else p2 = he1;
    }

    return p1;
  }
};
